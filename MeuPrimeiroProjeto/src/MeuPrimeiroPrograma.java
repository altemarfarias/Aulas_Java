public class MeuPrimeiroPrograma {

    public static void main(String[] args) {
        var login = "ola, estou declarando uma variavel";
        var senha = 5;
        System.out.println(login);
        System.out.println(senha);

        // ALGUNS TIPOS DE VARIATES
        // Texto (Strings)
        // Numero (Int)
        // Numero Fracionado (Double)
        // Booleanos (Verdadeiro / False)

        // NO JAVA MAIS ANTIGO ERA NECESSARILY
        // DECLARAR O TIPO DA VARIETAL
        String login2 = "altemar";
        int numero2 = 10;
        double porcentual = 20.30;
        boolean estado = true;
        System.out.println(login2);
        System.out.println(numero2);
        System.out.println(porcentual);
        System.out.println(estado);
        // DECLARATOR DE VARIAVEL
        int numero3;
        numero3 = 25;
        System.out.println(numero3);
    }
}
