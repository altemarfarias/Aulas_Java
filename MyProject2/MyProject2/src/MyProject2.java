public class MyProject2 {

    public static void main(String[] args){
        sum(10,15);
        System.out.println("---------");
        sum(45,65);
    }
    static void sum(int x, int y){
        System.out.println(x + y);
        System.out.println(x * y);
    }
    // static void => declaração padrão
    // sum => nome da função, poderia ser outro nome qualquer (soma em inglês)
    // entre parenteses () é definido os paranmetros que vai receber

}
